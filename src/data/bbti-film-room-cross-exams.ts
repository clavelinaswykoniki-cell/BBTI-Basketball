function resolveTemplate(
  template: CrossExamTemplate,
  source: BbtiFilmRoomCrossExam["source"],
  primaryPole?: PoleKey,
): BbtiFilmRoomCrossExam {
  return {
    ...template,
    standard: template.standard ?? (primaryPole ? STANDARD_BY_POLE[primaryPole] : "评价标准是否前后一致"),
    source,
  };
}

export function getBbtiFilmRoomCrossExam(clip: BbtiFilmRoomClip): BbtiFilmRoomCrossExam {
  const primaryPole = clip.coachTimeout.poles[0]?.pole;
  const byQuestion = QUESTION_CROSS_EXAMS[clip.questionId];
  const questionTemplate = primaryPole ? byQuestion?.[primaryPole] : undefined;
  if (questionTemplate) return resolveTemplate(questionTemplate, "question", primaryPole);

  const firstQuestionTemplate = byQuestion ? Object.values(byQuestion)[0] : undefined;
  if (firstQuestionTemplate) return resolveTemplate(firstQuestionTemplate, "question", primaryPole);

  const poleTemplate = primaryPole ? POLE_FALLBACKS[primaryPole] : undefined;
  if (poleTemplate) return resolveTemplate(poleTemplate, "pole", primaryPole);

  return {
    title: "标准复核",
    standard: "评价标准是否前后一致",
    question: "如果把这套标准换到另一个球星或另一轮系列赛，你还会保持同一个判罚尺度吗？",
    counterPunch: "对方会追问一致性：开庭最怕临场换标准。",
    seat: "战术席",
    source: "pole",
  };
}
