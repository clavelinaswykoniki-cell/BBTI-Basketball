function priorityBoost(
  input: ResolveBbtiAddFilesSuggestionsInput,
  suggestion: BbtiAddFilesSuggestion,
): number {
  if (suggestion.stage === "shipped") return -120;
  if (suggestion.id === "compare-report-polish" && input.hasPendingCompare) return 18;
  if (suggestion.id === "case-postgame-recap" && input.primaryChallengeTitle) return 10;
  if (suggestion.id === "case-postgame-replay-index" && input.primaryChallengeTitle) return 5;
  if (suggestion.id === "answer-poll-trend" && input.hasFilmRoomClips) return 12;
  if (suggestion.id === "answer-poll-trend" && !input.hasFilmRoomClips) return -20;
  if (suggestion.id === "return-streaks" && !input.hasPendingCompare) return 4;
  if (suggestion.id === "duo-chemistry" && input.hasPendingCompare) return 34;
  if (suggestion.id === "duo-chemistry" && !input.hasPendingCompare) return -8;
  if (suggestion.id === "film-room-quality" && input.hasFilmRoomClips) return 24;
  if (suggestion.id === "film-room-quality" && !input.hasFilmRoomClips) return -30;
  if (suggestion.id === "rivalry-scripts" && input.primaryChallengeTitle) return 20;
  if (suggestion.id === "rivalry-scripts" && !input.primaryChallengeTitle) return -10;
  if (suggestion.id === "share-card-export") return 4;
  return 0;
}

export function resolveBbtiAddFilesSuggestions(
  input: ResolveBbtiAddFilesSuggestionsInput,
): BbtiAddFilesSuggestion[] {
  return [...BASE_SUGGESTIONS]
    .map((suggestion) => ({
      ...suggestion,
      priority: suggestion.priority + priorityBoost(input, suggestion),
    }))
    .sort((a, b) => b.priority - a.priority || a.stage.localeCompare(b.stage) || a.id.localeCompare(b.id))
    .slice(0, 3);
}

export function buildBbtiAddFilesSuggestionCopy({
  code,
  suggestion,
  typeName,
}: {
  code: string;
  suggestion: BbtiAddFilesSuggestion;
  typeName: string;
}): string {
  return [
    "BBTI 下次加练单",
    `类型：${code} ${typeName}`,
    `加练：${suggestion.title}`,
    `状态：${suggestion.stage === "shipped" ? "已落地" : "下一步"}`,
    `理由：${suggestion.body}`,
    `过线：${suggestion.acceptance}`,
    `建议文件：${suggestion.targetFiles.join(", ")}`,
    `验证：${suggestion.validators.join(" && ")}`,
  ].join("\n");
}
